const uniswapV2RouterAddr = ethers.utils.getAddress(UNISWAP_V2_ROUTER_ADDR);
const uniswapV2RouterInterface = new Interface(UNISWAP_V2_ROUTER_ABI);

async function getTransactionStatus(transactionHash) {
  const receipt = await provider.getTransactionReceipt(transactionHash);
  return receipt.status ? "Success" : "Failed";
}

async function handleTransaction(transaction) {
  try {
    const decodedInput = uniswapV2RouterInterface.parseTransaction(transaction);
    const funcName = decodedInput.name;
    const args = decodedInput.args;

    const swapFunctions = [
      "swapExactETHForTokens",
      "swapExactETHForTokensSupportingFeeOnTransferTokens",
      "swapExactTokensForETH",
      "swapExactTokensForETHSupportingFeeOnTransferTokens",
      "swapExactTokensForTokens",
      "swapExactTokensForTokensSupportingFeeOnTransferTokens",
      "swapTokensForExactETH",
      "swapTokensForExactTokens",
    ];

    if (swapFunctions.includes(funcName)) {
      const status = await getTransactionStatus(transaction.hash);
      const tokens = getTokensInvolved(funcName, args);

      console.log("Transaction Hash:", transaction.hash);
      console.log("Status:", status);
      console.log("Function:", funcName);
      console.log("ERC-20 Tokens Transferred:", tokens);
    }
  } catch (error) {
    console.warn("Unable to decode transaction input data:", error);
  }
}

function getTokensInvolved(funcName, args) {
  if (
    funcName === "swapExactETHForTokens" ||
    funcName === "swapExactETHForTokensSupportingFeeOnTransferTokens"
  ) {
    return [args.path[0], args.path[args.path.length - 1]];
  }

  if (
    funcName === "swapExactTokensForETH" ||
    funcName === "swapExactTokensForETHSupportingFeeOnTransferTokens" ||
    funcName === "swapExactTokensForTokens" ||
    funcName === "swapExactTokensForTokensSupportingFeeOnTransferTokens"
  ) {
    return [args.path[0], args.path[args.path.length - 1]];
  }

  if (
    funcName === "swapTokensForExactETH" ||
    funcName === "swapTokensForExactTokens"
  ) {
    return [args.path[0], args.path[args.path.length - 1]];
  }

  return [];
}

async function monitorTransactions() {
  let latestBlock = await provider.getBlockNumber();

  while (true) {
    const currentBlock = await provider.getBlockNumber();

    for (
      let blockNumber = latestBlock;
      blockNumber <= currentBlock;
      blockNumber++
    ) {
      const block = await provider.getBlockWithTransactions(blockNumber);

      for (const transaction of block.transactions) {
        if (
          transaction.to &&
          uniswapV2RouterAddr === ethers.utils.getAddress(transaction.to)
        ) {
          await handleTransaction(transaction);
        }
      }
    }

    latestBlock = currentBlock;
  }
}

monitorTransactions().catch((error) => {
  console.error("Error monitoring transactions:", error);
});
